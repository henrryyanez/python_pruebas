#Operadores de Asignación, aumentamos un determinado valor

a = 0

a += 5    #Suma en asignación
a -= 2    #Resta en asignación
a *= 3    #Multiplicación en asignación
a /= 3    #División en asignación
a **=2    #Potencia en asignación, elevamos al cuadrado
a %=2     #Modullo en asignación, residuo de la división

print(a)
