#Stripping Whitespace, Espacions en blanco, con el metodo rstrip()

favorite_language = 'python '

print(favorite_language)
print(favorite_language.rstrip()) #Quita espacio en blanco de la derecha
print(favorite_language.lstrip()) #Quita espacio en blanco de la izquierda
print(favorite_language.strip())  #Quita espacio en blanco de los 2 lados

